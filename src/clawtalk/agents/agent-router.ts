/**
 * ExecutionContext encapsulates all Talk-related context provided to the agent.
 * For Main channel execution (context=null), this is omitted entirely.
 */
export interface ExecutionContext {
  /** System prompt prefix for this Talk (goal, rules, summary, sources) */
  systemPrompt: string;

  /** Tool definitions for reading Talk context (always included for Talk execution) */
  contextTools: LlmToolDefinition[];

  /** Tool definitions for data connectors bound to this Talk */
  connectorTools: LlmToolDefinition[];

  /** Conversation history for this Talk */
  history: LlmMessage[];
}

/**
 * ExecutionEvent is emitted during agent execution to stream progress to the caller.
 */
export type ExecutionEvent =
  | {
      type: 'started';
      runId: string;
      agentId: string;
      providerId: string;
      modelId: string;
    }
  | {
      type: 'text_delta';
      text: string;
    }
  | {
      type: 'tool_call';
      toolName: string;
      arguments: Record<string, unknown>;
    }
  | {
      type: 'tool_result';
      toolName: string;
      result: string;
      isError?: boolean;
      durationMs?: number;
    }
  | {
      type: 'awaiting_confirmation';
      confirmationId: string;
      toolName: string;
      actionSummary: string;
    }
  | {
      type: 'usage';
      inputTokens: number;
      outputTokens: number;
      estimatedCostUsd?: number;
    }
  | {
      type: 'completed';
      content: string;
      completion?: {
        completionStatus: 'complete' | 'incomplete';
        providerStopReason?: string | null;
        incompleteReason?: 'truncated' | 'empty' | 'unknown' | null;
      };
    }
  | {
      type: 'failed';
      errorCode: string;
      errorMessage: string;
      completion?: {
        completionStatus: 'complete' | 'incomplete';
        providerStopReason?: string | null;
        incompleteReason?: 'truncated' | 'empty' | 'unknown' | null;
      };
    }
  | {
      type: 'cancelled';
    };

/**
 * Final result of an agent execution.
 */
export interface AgentExecutionResult {
  /** The final content generated by the agent */
  content: string;

  /** Execution metadata */
  agentId: string;
  providerId: string;
  modelId: string;

  /** Token usage and estimated cost */
  usage?: {
    inputTokens: number;
    outputTokens: number;
    estimatedCostUsd?: number;
  };
  completion?: {
    completionStatus: 'complete' | 'incomplete';
    providerStopReason?: string | null;
    incompleteReason?: 'truncated' | 'empty' | 'unknown' | null;
  };
  /**
   * Provider-specific blobs to persist in the trusted replay store so
   * the next turn can replay them. Currently used by the codex_responses
   * path to carry forward encrypted reasoning items + assistant message
   * items for prefix-cache + chain-of-thought continuity.
   */
  providerData?: {
    codexReasoningItems?: Array<Record<string, unknown>>;
    codexMessageItems?: Array<Record<string, unknown>>;
  };
}

export interface AgentExecutionOptions {
  runId: string;
  userId: string;
  signal?: AbortSignal;
  emit?: (event: ExecutionEvent) => void;
  executeToolCall?: (
    toolName: string,
    args: Record<string, unknown>,
  ) => Promise<{ result: string; isError?: boolean }>;
  alwaysAllowedContextToolNames?: string[];
  maxToolIterations?: number;
  toolIterationLimitFallback?: string;
  /**
   * When true, set provider-specific `tool_choice=required` on the
   * FIRST iteration of the tool-calling loop. Subsequent iterations
   * fall back to the default 'auto' — once the agent has been pushed
   * into picking a tool, we let it decide whether to call more tools
   * or settle on a final assistant turn.
   */
  forceToolUseOnFirstIteration?: boolean;
  /**
   * Snapshot of the credential kind the resolver should pick for this run.
   * Null/undefined falls back to the live agent value.
   */
  credentialKindSnapshot?: 'api_key' | 'subscription' | null;
  /**
   * Explicit credential scope for provider secrets. Runtime paths fail closed
   * for personal/workspace credentials when this is omitted.
   */
  credentialScope?: ExecutionCredentialScope | null;
  /**
   * The Talk's effective tool set for this turn. Absent => no non-internal
   * tools are exposed.
   */
  effectiveTools?: EffectiveToolAccess[];
  /**
   * Run-scoped deadline budget (T6). When present, the tool loop stops at a
   * step boundary once the run's external-await budget is exhausted instead of
   * starting another LLM + tool round. Tool legs (web_search) draw their own
   * per-step caps from the same budget. Absent for direct/test callers.
   */
  budget?: DeadlineBudget;
}

export const ALWAYS_ALLOWED_CONTEXT_TOOLS = new Set([
  'read_source',
  // Content-feature edit tool — registered only when
  // the Talk has an attached Content doc (`hasContent === true`). Never
  // appears in any tool family, so
  // without this allowlist it gets silently filtered out for every
  // agent.
  'apply_content_edit',
]);

/**
 * Tools that are SAFE TO RUN CONCURRENTLY with each other within a single
 * assistant turn's tool batch. A maximal run of CONSECUTIVE listed tools
 * executes in parallel (bounded by READ_ONLY_TOOL_CONCURRENCY); every other
 * tool is a barrier that runs sequentially. See the dispatch loop below.
 *
 * Membership is an EXPLICIT, per-tool correctness assertion. There is NO name,
 * prefix, or family inference: a tool absent from this set runs sequentially
 * even if it "looks" read-only. To add a tool here it must be:
 *   - side-effect-free on EVERY path, including error/retry paths (it mutates
 *     no Talk / document / external / stored-credential state — reruns and
 *     concurrent siblings are observationally equivalent), and
 *   - safe to run concurrently with its own siblings.
 *
 * Deliberately conservative — only the two tools we can assert are pure:
 *   - read_source: a single SELECT over context_sources.
 *   - web_search: resolves credentials in a short tx that COMMITS before the
 *     provider fetch (T2/#622) and never mutates stored secrets; the fetch is
 *     the latency parallelism actually hides.
 * The Google read tools are NOT listed: their 401-retry path can refresh OR
 * DELETE the stored OAuth credential and bypasses the in-flight-refresh
 * dedupe (google-drive-tools.ts -> google-tools-service.ts), so concurrent
 * Google reads can stampede a credential write. Parallelizing them is a
 * follow-up gated on routing that retry through the deduped refresh.
 *
 * This set changes ONLY execution concurrency. Results are always appended to
 * the message history in batch (tool_use id) order regardless of completion
 * order, so the prefix stays byte-stable for prompt caching (T4 / Lane A). Do
 * not use this set to reorder results.
 */
export const READ_ONLY_PARALLELIZABLE_TOOLS = new Set<string>([
  'read_source',
  'web_search',
]);

/**
 * Upper bound on read-only tool calls executed concurrently within one
 * contiguous run. Caps provider-quota and request-DB connection-queue pressure
 * when a model emits a large tool batch in a single turn (a hostile or confused
 * model can request many web_search calls at once). Small because the shared
 * request DB client is max:1 and search providers rate-limit.
 */
export const READ_ONLY_TOOL_CONCURRENCY = 6;

/**
 * Run `fn` over `items` with at most `limit` concurrent executions, returning
 * results in INPUT order (never completion order). Used to bound and order the
 * parallel execution of a contiguous run of read-only tool calls so the
 * appended results stay byte-stable for prompt caching.
 */
async function runBoundedInOrder<T, R>(
  items: T[],
  limit: number,
  fn: (item: T) => Promise<R>,
): Promise<R[]> {
  const results = new Array<R>(items.length);
  let cursor = 0;
  const worker = async (): Promise<void> => {
    while (cursor < items.length) {
      const index = cursor;
      cursor += 1;
      results[index] = await fn(items[index]);
    }
  };
  const workerCount = Math.min(Math.max(1, limit), items.length);
  await Promise.all(Array.from({ length: workerCount }, () => worker()));
  return results;
}

const CLEAN_PROVIDER_STOP_REASONS = new Set([
  'stop',
  'end_turn',
  'stop_sequence',
]);

/**
 * Max number of times we'll silently retry an incomplete Codex
 * response before surfacing it as `incomplete_response`. Mirrors
 * hermes' 3-retry continuation budget. A Codex "incomplete" usually
 * means the model produced commentary or reasoning-only output and
 * needs one more turn to land on a final answer — feeding back the
 * encrypted reasoning items lets the backend keep its chain coherent.
 */
const MAX_CODEX_CONTINUATIONS = 3;

function normalizeProviderStopReason(value: string): string | null {
  const normalized = value.trim();
  return normalized.length > 0 ? normalized : null;
}

function buildIncompleteCompletion(input: {
  content: string;
  providerStopReason: string | null;
}): {
  completionStatus: 'incomplete';
  providerStopReason?: string | null;
  incompleteReason?: 'truncated' | 'empty' | 'unknown' | null;
  errorMessage: string;
} {
  const hasVisibleText = input.content.trim().length > 0;
  if (!hasVisibleText) {
    return {
      completionStatus: 'incomplete',
      providerStopReason: input.providerStopReason,
      incompleteReason: 'empty',
      errorMessage:
        'The model finished without producing a usable final response.',
    };
  }

  if (
    input.providerStopReason === 'length' ||
    input.providerStopReason === 'max_tokens'
  ) {
    return {
      completionStatus: 'incomplete',
      providerStopReason: input.providerStopReason,
      incompleteReason: 'truncated',
      errorMessage: `The model stopped before finishing its answer (provider stop reason: ${input.providerStopReason}).`,
    };
  }

  return {
    completionStatus: 'incomplete',
    providerStopReason: input.providerStopReason,
    incompleteReason: 'unknown',
    errorMessage: input.providerStopReason
      ? `The model stopped without a clean completion signal (provider stop reason: ${input.providerStopReason}).`
      : 'The model stopped without a clean completion signal.',
  };
}

